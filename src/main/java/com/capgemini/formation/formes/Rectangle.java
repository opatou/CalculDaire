package com.capgemini.formation.formes;

public class Rectangle  {

		int largeur = 0;
		int longueur = 0;
	
	public Rectangle (int largeur, int longueur){
		this.largeur = largeur;
		this.longueur = longueur;
	}
	
	public int getLargeur(){
		return largeur;
	}
	
	public int getLongueur(){
		return longueur;
	}
	
	
}
